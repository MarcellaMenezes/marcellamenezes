# O Python trabalha tipos de valores. Com os valores abaixo, dê o nome de seus tipos:

# a. 1      = inteiro  (int)
print(type(1))

# b. 12.6   = Real     (float)
print(type(12.6))

# c. True   = Booleano (bool)
print(type(True))

# d. False  = Booleano (bool)
print(type(False))

# e. -543   = inteiro  (int)
print(type(-543))

# f. -5.78      = Real  (float)
print(type(-5.78))

# g. “copo”     = texto    (string)
print(type("copo"))

# h. ‘Belo dia’ = texto    (string)
print(type("Belo dia"))

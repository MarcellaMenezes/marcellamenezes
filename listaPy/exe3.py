# a. Operadores matemáticos

print(f'i. 10 + 3 = {10 + 3}')
print(f'ii. 10 - 3= {10 - 3}')
print(f'iii. 10 * 3 = {10 * 3}')
print(f'iv. 10 / 3 = {10 / 3}')
print(f'v. 10 / 3.0 = {10 / 3.0}')
print(f'vi. 13 / 3 = { 13 / 3}')
print(f'vii. 13 / 3.0 = { 13 / 3.0 }')
print(f'viii. 13 // 3.0 = {13 // 3.0}')

# b. Ordem dos operadores
print(f' i. 5 + 30 * 20 = {5 + 30 * 20}')
print(f' ii. (5 + 30) * 20 = {(5 + 30) * 20 }')
print(f' iii. ((5 + 30) * 20) / 10 = {((5 + 30) * 20) / 10 }')
print(f' iv. 5 + 30 * 20 / 10 = {5 + 30 * 20 / 10 }')

# c. Operadores comparação
print(f'i. 2 < 3 = {2 < 3}')
print(f'ii. 9 > 8 = {9 > 8}')
print(f'iii. 1 == 1 = {1 == 1}')
print(f'iv. 1 != 2 = {1 != 2}')
print(f'v. 1 != 1 = {1 != 1}')
print(f'vi. 4 <= 4 = {4 <= 4}')
print(f'vii. 5 >= 6 = { 5 >= 6}')
print(f'viii. 1 < 2 < 3 = {1 < 2 < 3}')
print(f'ix. 1 < 2 < 2 = { 1 < 2 < 2}')
print(f'x. 1 + 2 < 25 / 5 = {1 + 2 < 25 / 5}')

# d. Mais operadores matemáticos:
print(f'i. 2 ** 4 = {2 ** 4}')
print(f'ii. 26 % 5 = {26 % 5}')

# e. Operadores lógicos
print(f'i. not True = {not True}')
print(f'ii. not False = {not False}')
print(f'iii. True and True = {True and True}')
print(f'iv. True and False = {True and False}')
print(f'v. False and True = {False and True}')
print(f'vi. False and False = {False and False}')
print(f'vii. True or True = {True or True }')
print(f'viii. True or False = {True or False}')
print(f'ix. False or True = {False or True}')
print(f'x. False or False = {False or False}')
print(f'xi. True or True and False = {True or True and False}')
print(f'xii. (True or True) and False = {(True or True) and False}')
print(f'xiii. not True or False = {not True or False}')
print(f'xiv. not (True or False) = {not (True or False)}')
print(f'xv. not (True and False) and (True or False) = {not (True and False) and (True or False)}')
print(f'xvi.1 > 2 and 3 > 4 = {1 > 2 and 3 > 4 }')
print(f'xvii. 1 > 2 and 3 < 4 = {1 > 2 and 3 < 4}')
print(f'xviii. 1 < 2 and 3 < 4 = {1 < 2 and 3 < 4}')
print(f'xix. 1 + 2 and 3 + 4 = {1 + 2 and 3 + 4}') #AND da direita para a esquerda
print(f'xx. 1 + 2 or 3 + 4 = {1 + 2 or 3 + 4}')    #OR da esquerda par a direita, se o primeiro elemento for 0, ele passa para o próximo
print(f'xxi. 0 or 3 + 4 = { 0 or 3 + 4}')
print(f'xxii. 0 and 3 + 4 = { 0 and 3 + 4}')
print(f'xxiii.True and 3 > 5 = {True and 3 > 5}')
print(f'xxiv. False and 3 > 5 = {False and 3 > 5}')